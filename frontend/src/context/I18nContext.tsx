function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function detectDefaultLanguage(): Language {
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored === "en" || stored === "km") return stored;
  const navLang = (navigator.language || "").toLowerCase();
  if (navLang.startsWith("km") || navLang.startsWith("kh")) return "km";
  return "en";
}

type I18nValue = {
  lang: Language;
  setLang: (_lang: Language) => void;
  t: (_key: string, _vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Language>(() => detectDefaultLanguage());

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, lang);
  }, [lang]);

  const setLang = useCallback((next: Language) => {
    setLangState(next);
  }, []);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const dict = DICTS[lang];
      const template = dict[key] ?? DICTS.en[key] ?? key;
      if (!vars) return template;
      return Object.entries(vars).reduce((acc, [k, v]) => {
        const pattern = new RegExp(`\\{${escapeRegExp(k)}\\}`, "g");
        return acc.replace(pattern, String(v));
      }, template);
    },
    [lang],
  );

  const value = useMemo<I18nValue>(() => ({ lang, setLang, t }), [lang, setLang, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

// eslint-disable-next-line react-refresh/only-export-components
export function useI18n(): I18nValue {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}

